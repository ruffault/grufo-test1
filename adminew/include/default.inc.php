<p>Historique de toutes les commandes qui ont étés passées sur le site. Il
suffit de sélectionné une ancienne commande pour obtenir toutes les 
informations la concernant : acheteurs, produits, montant, adresse de
livraison...</p>

<h3><img src='css/stat.gif' alt="" /><a href='index2.php?page=stat'>Statistiques</a></h3>
<p>Statistiques sur la fréquentation du site et les comportements d'achat.
</p>

<h3><img src='css/mail.gif' alt="" /><a href='index2.php?page=list'>Mailing-list</a></h3>
<p>Gestion de la liste de diffusion. C'est ici que l'on rédige et que l'on émet
les mails à caractère publicitaires. Tous les clients abonnés à la
<em>Mailing List</em> recevront ces mails.</p>

<h3><img src='css/quit.gif' alt="" /><a href='logout.php'>Quitter</a></h3>
<p>Fin de session sur la partie administrative du site. Il est indispensable de
fermer votre session lorsque vous n'utilisez plus le site. En restant logué
sur la partie administrative du site, vous exposez le site à des attaques.
Aillez donc le bon réflexe et veillez à bien vous déconnecter à l'aide du 
bouton <em>quitter</em> dès que vous n'utilisez pas l'application.</p>
</div>

