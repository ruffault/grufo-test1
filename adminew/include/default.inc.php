<h3><img src='css/add.gif' alt="" /><a href='index2.php?page=addproduct'>Ajouter</a></h3>
<p>Formulaire d'ajout de nouveaux produits. Il faut prévoir ici la référence
du produit à ajouter, son prix, son nom, son auteur et son éditeur. Une fois le
produit ajouter, celui-ci est directement disponible en ligne. Les internautes
peuvent alors le commander.</p>

<h3><img src='css/modif.gif' alt="" /><a href='index2.php?page=searchproduct'>Modifier</a></h3>
<p>Formulaire de recherche par nom, auteur, éditeur ou date. Une fois le produit
retrouvé, il est possible d'éditer ça fiche. Les modifications prendront alors
directement effet sur la partie publique du site. Dans cette partie, on peut 
aussi associer une image à un produit. Il suffit d'envoyer une image au
format jpeg et celle-ci sera automatiquement redimensionné.</p>

<h3><img src='css/categ.gif' alt="" /><a href='index2.php?page=categ'>Catégorie</a></h3>
<p>Gestion des catégories et des sous catégories du catalogue. Il suffit de 
sélectionner une catégorie <em>mère</em> et d'y mettre une catégorie
<em>fille</em>. On dit aussi imbriquer. Ainsi on imbrique
une sous catégorie dans une catégorie.</p>
<p>Il est aussi possible de créer de nouvelles catégories.</p>

<h3><img src='css/traite.gif' alt="" /><a href='index2.php?page=viewcommande'>A traiter</a></h3>
<p>Liste de toutes les commandes à préparer. Ici, on a toutes les
informations sur les commandes. On accède à un panneau de contrôle qui permet
de définir <em>l'état de validation</em> d'une commande, c'est à dire l'étape
où l'on se situe dans le traitement de celle-ci. A chaque action effectuée ici,
le client concerné reçoit un mail l'informant de l'état du traitement.</p>

</div>
<div id="adroite">
